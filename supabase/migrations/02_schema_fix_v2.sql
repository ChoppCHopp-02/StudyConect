-- 1. Patch the "posts" table
ALTER TABLE posts ADD COLUMN IF NOT EXISTS tag VARCHAR(255) DEFAULT NULL;
ALTER TABLE posts ADD COLUMN IF NOT EXISTS likes JSONB DEFAULT '[]'::jsonb;
ALTER TABLE posts ADD COLUMN IF NOT EXISTS group_id BIGINT DEFAULT NULL;
ALTER TABLE posts ADD COLUMN IF NOT EXISTS is_pinned BOOLEAN DEFAULT FALSE;

-- Add foreign key constraint for group_id if not already present
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM information_schema.table_constraints 
        WHERE constraint_name = 'fk_posts_group'
    ) THEN
        ALTER TABLE posts 
        ADD CONSTRAINT fk_posts_group 
        FOREIGN KEY (group_id) REFERENCES study_groups(id) ON DELETE CASCADE;
    END IF;
END $$;


-- 2. Patch the "messages" table
